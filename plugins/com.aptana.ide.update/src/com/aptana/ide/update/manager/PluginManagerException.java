package com.aptana.ide.update.manager;

public class PluginManagerException extends Exception
{

	private static final long serialVersionUID = -4346630570899919351L;

	public PluginManagerException(String string)
	{
		super(string);
	}

	public PluginManagerException(String string, Throwable cause)
	{
		super(string, cause);
	}

	public PluginManagerException(Throwable cause)
	{
		super(cause);
	}

}
