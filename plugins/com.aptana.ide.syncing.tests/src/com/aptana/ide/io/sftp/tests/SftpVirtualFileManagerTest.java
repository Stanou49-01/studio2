	/**
	 * Test method for 'com.aptana.ide.io.sftp.SftpVirtualFileManager.fromSerializableString(String)'
	 * 
	 * @throws ConnectionException
	 * @throws IOException 
	 */
	public void testFromSerializableString() throws ConnectionException, IOException
	{
		IVirtualFileManager fileManager = SftpProtocolManager.getInstance().createFileManager();
		SftpVirtualFileManager sftp = (SftpVirtualFileManager) fileManager;
		sftp.setNickName("nickname"); //$NON-NLS-1$
		sftp.setServer("server.host.name"); //$NON-NLS-1$
		sftp.setBasePath("/base/path"); //$NON-NLS-1$
		sftp.setUser("user"); //$NON-NLS-1$
		sftp.setPassword("password$$!!@@~~"); //$NON-NLS-1$
		// sftp.setId(0); // Id is auto-calculated
		sftp.setAutoCalculateServerTimeOffset(true);
		sftp.setTimeOffset(100000); // requires a valid server, so we have to fake a value
		sftp.setPort(sftp.getPort() + 1);

		IVirtualFile f1 = sftp.createVirtualFile("/test.txt"); //$NON-NLS-1$
		IVirtualFile f2 = sftp.createVirtualDirectory("/test_directory"); //$NON-NLS-1$
		sftp.addCloakedFile(f1);
		sftp.addCloakedFile(f2);

		sftp.addCloakExpression(".*\\.txt"); //$NON-NLS-1$
		sftp.addCloakExpression("\\.svn"); //$NON-NLS-1$
		
		String serialized = sftp.getHashString();
		IVirtualFileManager fileManager2 = SftpProtocolManager.getInstance().createFileManager();
		SftpVirtualFileManager sftp2 = (SftpVirtualFileManager) fileManager2;
		sftp2.fromSerializableString(serialized);

		String[] strings = serialized.split(SftpVirtualFileManager.DELIMITER);
		assertEquals(12, strings.length);

		assertEquals(sftp.getNickName(), sftp2.getNickName());
		assertEquals(sftp.getServer(), sftp2.getServer());
		assertEquals(sftp.getBasePath(), sftp2.getBasePath());
		assertEquals(sftp.getUser(), sftp2.getUser());
		assertEquals(sftp.getPassword(), sftp2.getPassword());
		assertEquals(sftp.isAutoCalculateServerTimeOffset(), sftp2.isAutoCalculateServerTimeOffset());
		assertEquals(sftp.getId(), sftp2.getId());
		assertEquals(sftp.getTimeOffset(), sftp2.getTimeOffset());
		assertEquals(sftp.getPort(), sftp2.getPort());
		assertEquals(sftp.getSavePassword(), sftp2.getSavePassword());

		assertEquals(2, sftp2.getCloakedFiles().length);
		assertEquals(2, sftp2.getCloakedFileExpressions().length);
	}

